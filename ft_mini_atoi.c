int	ft_mini_atoi(char *str)
{
	int				i;
	long int		nb;

	i = 0;
	nb = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	if (nb > 2147483647)
		return (-1);
	if (nb < -2147483648)
		return (0);
	return (nb);
}
